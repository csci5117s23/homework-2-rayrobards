import Head from 'next/head'
import {useEffect, useState} from "react";
import { useRouter } from 'next/router'
import { useAuth } from "@clerk/nextjs";
import { addUser } from '@/modules/Data'
import { SignedIn, SignedOut, useUser, RedirectToSignIn } from "@clerk/clerk-react";

export default function Home() {
  let router = useRouter()
  const { isLoaded, isSignedIn, user } = useUser();
  const {getToken} = useAuth();

  const [login, setLogin] = useState(false)

  function signIn()
  {
    setLogin(true)
  }

  function Redirect() {
      router.push('/todos')
  }

  function AddUser() {
    async function addUserDB() {
      if(isLoaded)
      { 
        const newUser = {
          "username": user.fullName,
          "_id":user.id,
        }
        const token = await getToken({template: "codehooks"});
        await addUser(token, newUser);
      }
    }
    addUserDB();
  }

  return (
    <>
      <Head>
          <title>home page</title>
          <meta name="description" content="Generated by create next app" />
          <meta name="viewport" content="width=device-width, initial-scale=1" />
          <link rel="icon" href="/favicon.ico" />
      </Head>
        <SignedIn>
          <AddUser />
          <Redirect />
        </SignedIn>
        <SignedOut>
          <div className='titlePage'>
            <div className='titlePageContent'>
              <h1>Ray's Todo App</h1>
              <button onClick={signIn}>Login or Signup</button>
              {login && (
                <RedirectToSignIn />
              )}
            </div>
          </div>
        </SignedOut>
    </>
  )
}
